#!/usr/bin/env python3
"""
Transformer + VQC Weight Generation — End-to-End Training Example
==================================================================

Demonstrates embedding VQCGeneratedLinear (VQC dynamically generated weights)
into a Transformer FFN, replacing traditional nn.Linear for quantum-classical
hybrid training.

Architecture:
  Input tokens
    → Embedding + Positional Encoding
    → Transformer Encoder × N
        ├── Multi-Head Self-Attention (standard nn.Linear)
        └── FFN:
            ├── nn.Linear(d_model → d_ff)           ← standard linear
            ├── ReLU
            └── VQCGeneratedLinear(d_ff → d_model)   ← VQC generated weights!
    → Classification Head
    → Output

Key design:
  - VQCGeneratedLinear holds a learnable context vector (replaces random features)
  - context → VQC → HyperNetwork dynamically generates weight matrix W
  - Entire pipeline is end-to-end trainable, gradients flow through VQC simulator

Usage:
  # Default hyperparameters
  python transformer_vqc.py

  # Custom hyperparameters
  python transformer_vqc.py --d_model 256 --d_ff 1024 --n_wires 10 --n_qlayers 3

  # Compare with standard Transformer baseline
  python transformer_vqc.py --baseline
"""

